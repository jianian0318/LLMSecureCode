import boto3

DYNAMO_CLIENT = boto3.client('dynamodb')


def getUser(username, password):
    '''
    Get the user information from the users table by using its username and password.
    '''
    response = DYNAMO_CLIENT.get_item(
        TableName='users',
        Key={
            'username': {'S': username},
            'password': {'S': password}
        }
    )

    user = response.get('Item')

    return user
