def printGreen(str, end="\n"):
    print(f'\033[32m{str}\033[0m', end=end)


def printRed(str, end="\n"):
    print(f'\033[31m{str}\033[0m', end=end)


def printYellow(str, end="\n"):
    print(f'\033[33m{str}\033[0m', end=end)


def printBlue(str, end="\n"):
    print(f'\033[34m{str}\033[0m', end=end)